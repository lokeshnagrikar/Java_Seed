package ClassWork_volume;

public class calciVol {
 
   
	//private static final double PI = 3.14;

	public double VolumeCalci(double radius, double height) {

		//float PI = 3.14f;
	return Math.PI * radius * height;
		
	}
	
	public int VolumeCalci(int  length, int breadth ,int height) {

		return length*breadth*height;
		
		
	}
	
}
